package chapter7;

public class calc_helper {
    public static double addVal(double d1, double d2) {
        return d1 + d2;
    }
    public static double subVal(double d1, double d2) {
        return d1 - d2;
    }
    public static double multVal(double d1, double d2) {
        return d1 * d2;
    }
    public static double divideVal(double d1, double d2) {
        return d1 / d2;
    }
}
